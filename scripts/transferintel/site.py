# The generated head block is delimited at both ends so a rerun can remove
# exactly what the previous run added, leaving the author's own whitespace and
# hand-written tags untouched.
GEN_START = "<!-- generated by scripts/render_site.py, do not hand edit -->"
GEN_END = "<!-- /generated -->"

STATUS_LABEL = {
    "rumor": "Rumour",
    "talks": "In talks",
    "agreed": "Fee agreed",
    "medical": "Medical booked",
    "confirmed": "Confirmed pending announcement",
    "done": "Completed",
    "collapsed": "Collapsed",
}

#: Short form for places where the full label will not fit, e.g. a table cell
#: or the funnel chips.
STATUS_SHORT = dict(STATUS_LABEL, confirmed="Confirmed")

STATUS_ORDER = ["rumor", "talks", "agreed", "medical", "confirmed", "done",
                "collapsed"]

#: TI-002. The one definition of which deals the value section assesses.
#: The scatter chart and the verdicts table both read this, which is the
#: whole point: they used to filter separately, and a table that disagreed
#: with the chart above it was indistinguishable from a table that was broken.
VALUE_STATUSES = frozenset({"agreed", "medical", "confirmed", "done"})


def value_eligible(deals: Iterable[Deal]) -> list[Deal]:
    """Deals with a confirmed fee worth assessing, best first.

    Free transfers are excluded deliberately rather than incidentally. A value
    score divides a fee by a rating, and a fee of zero makes every free signing
    an infinitely good deal, which is true in a sense that helps nobody.
    """
    rows = [
        d for d in deals
        if (d.fee or 0) > 0
        and str(getattr(d.status, "value", d.status)) in VALUE_STATUSES
    ]
    return sorted(rows, key=lambda d: (-(d.fee or 0), d.p))


# ---------------------------------------------------------------- config


@dataclass
class SiteConfig:
    """Everything the renderer needs that is not in the deal data.

    `base_url` is the one value that has to be right. Canonical tags, Open
    Graph URLs and the sitemap all require absolute URLs, and a wrong origin
    is worse than none: it tells crawlers the real page is somewhere else.
    """

    base_url: str = ""
    title: str = "TransferIntel"
    description: str = (
        "Premier League transfer rumours scored 0 to 100 for credibility, "
        "with transparent value-for-money verdicts on confirmed fees."
    )
    author: str = "Peter Brendan"
    twitter: str = ""
    locale: str = "en_GB"

    #: TI-010. The provider's hosted form endpoint. Empty means no capture
    #: form is rendered at all, which is the right default: a form that posts
    #: nowhere collects addresses into a void and burns the one chance you get
    #: to ask a visitor for their email.
    newsletter_action: str = ""
    newsletter_provider: str = ""

    #: TI-012. Cloudflare Web Analytics token. Empty means no snippet.
    analytics_token: str = ""
    goatcounter: str = ""

    @classmethod
    def from_data(cls, raw: dict) -> "SiteConfig":
        config = raw.get("config") or {}
        site = config.get("site") or {}
        news = config.get("newsletter") or {}
        stats = config.get("analytics") or {}
        return cls(
            base_url=str(site.get("baseUrl", "")).rstrip("/"),
            title=site.get("title", cls.title),
            description=site.get("description", cls.description),
            author=site.get("author", cls.author),
            twitter=site.get("twitter", ""),
            locale=site.get("locale", cls.locale),
            newsletter_action=str(news.get("action", "")),
            newsletter_provider=str(news.get("provider", "")),
            analytics_token=str(stats.get("cloudflareToken", "")),
            goatcounter=str(stats.get("goatcounter", "")),
        )

    @property
    def path_prefix(self) -> str:
        """The sub-path a project site is served from, e.g. /transferintel."""
        if not self.base_url:
            return ""
        after_scheme = self.base_url.split("://", 1)[-1]
        slash = after_scheme.find("/")
        return "" if slash == -1 else after_scheme[slash:].rstrip("/")

    def url(self, path: str) -> str:
        return f"{self.base_url}/{path.lstrip('/')}" if self.base_url else f"/{path.lstrip('/')}"

    def href(self, path: str) -> str:
        """Root-relative link, correct whether or not the site sits in a sub-path."""
        return f"{self.path_prefix}/{path.lstrip('/')}" if path != "/" else f"{self.path_prefix}/"


def slugify(value: str) -> str:
    """URL-safe slug.

    Uses the same transliteration table as entity resolution rather than a
    second copy. NFKD does not decompose characters like O-slash, so a naive
    normalise silently drops them: Odegaard would slug as "degaard", losing
    the first letter of the name and risking a collision with a different
    player. One table, used everywhere, or this bug comes back.
    """
    norm = fold_for_slug(value)
    norm = re.sub(r"[^a-z0-9]+", "-", norm).strip("-")
    return norm or "item"


def e(value) -> str:
    return html.escape(str(value if value is not None else ""), quote=True)


# ------------------------------------------------------- HTML injection


class _Locator(HTMLParser):
    """Find an element by id and report where its content begins and ends.

    A regex over HTML is how you corrupt a file the first time someone adds an
    attribute or a nested tag. This walks the document properly and counts
    depth, so injecting into `<div id="feed">` still works after the div grows
    children or the attribute order changes.
    """

    def __init__(self, target_id: str) -> None:
        super().__init__(convert_charrefs=False)
        self.target = target_id
        self.tag: str | None = None
        self.depth = 0
        self.start: tuple[int, int] | None = None
        self.end: tuple[int, int] | None = None

    def handle_starttag(self, tag, attrs):
        attrs_d = dict(attrs)
        if self.tag is None and attrs_d.get("id") == self.target:
            self.tag = tag
            self.depth = 1
            line, col = self.getpos()
            self.start = (line, col + len(self.get_starttag_text() or ""))
        elif self.tag is not None and tag == self.tag:
            self.depth += 1

    def handle_startendtag(self, tag, attrs):
        if dict(attrs).get("id") == self.target and self.tag is None:
            self.tag = tag
            self.start = None  # self-closing, nothing to fill

    def handle_endtag(self, tag):
        if self.tag is not None and tag == self.tag and self.end is None:
            self.depth -= 1
            if self.depth == 0:
                self.end = self.getpos()


def _offset(text: str, line: int, col: int) -> int:
    lines = text.splitlines(keepends=True)
    return sum(len(l) for l in lines[: line - 1]) + col


def inject_into(document: str, element_id: str, content: str) -> str:
    """Replace the inner HTML of `element_id`. Returns the document unchanged
    if the element is not found, because a missing container is a template the
    author has edited, not a reason to fail a daily build."""
    loc = _Locator(element_id)
    try:
        loc.feed(document)
        loc.close()
    except Exception:
        return document
    if not loc.start or not loc.end:
        return document
    start = _offset(document, *loc.start)
    end = _offset(document, *loc.end)
    if start > end:
        return document
    return document[:start] + content + document[end:]


def inject_head(document: str, content: str) -> str:
    idx = document.lower().rfind("</head>")
    if idx == -1:
        return document
    return document[:idx] + content + document[idx:]


# ------------------------------------------------------------- fragments


def deal_slug(deal: Deal) -> str:
    return slugify(deal.id)


def deal_path(deal: Deal) -> str:
    return f"deals/{deal_slug(deal)}/"


def club_path(club: str) -> str:
    return f"clubs/{slugify(club)}/"


def cred_class(cred: int) -> str:
    if cred >= 75:
        return "high"
    if cred >= 50:
        return "mid"
    if cred >= 25:
        return "low"
    return "vlow"


def deal_sentence(deal: Deal) -> str:
    """One plain sentence per deal, the thing a crawler and a screen reader
    both actually read."""
    fee = f" for a reported £{deal.fee:g}m" if deal.fee else ""
    status = STATUS_LABEL.get(
        deal.status.value if hasattr(deal.status, "value") else str(deal.status),
        str(deal.status),
    )
    return (
        f"{deal.p}, {deal.pos or 'player'}"
        f"{f', age {deal.age}' if deal.age else ''}, "
        f"from {deal.from_club} to {deal.to}{fee}. "
        f"Status: {status}. Credibility {deal.cred} out of 100, "
        f"based on {deal.src or 'reported sources'}."
    )


# ------------------------------------------------------------- value model


def value_score(deal: Deal, proven_clubs: Iterable[str] = ()) -> int:
    """The site's transparent value rating, 5 to 98.

    This is a deliberate second implementation of the model in index.html.
    Duplication is normally a smell; here it is the test. The browser copy is
    what a reader sees and the Python copy is what the build can assert
    against, and `test_site.py` checks that both produce the same numbers for
    every deal in the dataset. If someone edits one and not the other, the
    build says so.
    """
    score = 50.0
    peak = 25
    if deal.age <= 24:
        score += (24 - deal.age) * 2.2
    else:
        score -= abs(deal.age - peak) * 3.2
    if deal.pos in {"AM", "ST", "LW", "RW"}:
        score += 6
    if deal.pos == "CM":
        score += 4
    if deal.from_club in set(proven_clubs):
        score += 8
    if deal.age >= 30:
        score -= 8
    return max(5, min(98, round(score)))


#: Fee per value point, and what that ratio is called. Ordered ascending.
VERDICT_BANDS: tuple[tuple[float, str, str], ...] = (
    (0.55, "Bargain", "v-bargain"),
    (0.95, "Fair", "v-fair"),
    (1.35, "Premium", "v-premium"),
    (float("inf"), "Overpay risk", "v-overpay"),
)


def verdict(deal: Deal, proven_clubs: Iterable[str] = ()) -> tuple[str, str]:
    if not deal.fee:
        return "Bargain", "v-bargain"
    ratio = deal.fee / value_score(deal, proven_clubs)
    for limit, label, css in VERDICT_BANDS:
        if ratio < limit:
            return label, css
    return "Overpay risk", "v-overpay"


#: TI-002. Never render bare headers. A table with a header row and no body
#: is indistinguishable from a broken page, and it was one.
VALUE_EMPTY_ROW = (
    '<tr class="empty"><td colspan="6">No confirmed fees yet this window. '
    "Free transfers are excluded from value scoring.</td></tr>"
)


def render_value_rows(deals: list[Deal], proven_clubs: Iterable[str] = ()) -> str:
    """The verdicts table body, pre-rendered from the same collection the
    scatter chart uses."""
    rows = value_eligible(deals)
    if not rows:
        return VALUE_EMPTY_ROW
    out = []
    for d in rows:
        label, css = verdict(d, proven_clubs)
        out.append(
            f'<tr class="clickable" data-id="{e(d.id)}">'
            f"<td><b>{e(d.p)}</b></td>"
            f'<td class="move">{e(d.from_club)} to {e(d.to)}</td>'
            f"<td>{d.age}</td>"
            f"<td>£{d.fee:g}m</td>"
            f"<td>{value_score(d, proven_clubs)}</td>"
            f'<td><span class="verdict {css}">{e(label)}</span></td></tr>'
        )
    return "\n".join(out)


