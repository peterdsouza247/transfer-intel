def render_sitemap(cfg: SiteConfig, entries: list[tuple[str, str, str]]) -> str:
    """entries: (path, lastmod ISO date, changefreq)."""
    urls = "".join(
        f"<url><loc>{e(cfg.url(path))}</loc><lastmod>{e(lastmod)}</lastmod>"
        f"<changefreq>{freq}</changefreq></url>"
        for path, lastmod, freq in entries
    )
    return (
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">'
        f"{urls}</urlset>\n"
    )


def render_robots(cfg: SiteConfig) -> str:
    """Permissive, with the sitemap declared.

    The AI crawlers are listed explicitly and allowed. That is a judgment
    call, not a default: for a site whose entire purpose is to be cited as a
    credibility source, being quotable by an assistant is distribution, not
    theft. Flip any of these to Disallow if you decide otherwise.
    """
    sitemap = cfg.url("sitemap.xml")
    return f"""# {cfg.title}
User-agent: *
Allow: /

# Assistants and answer engines. Allowed deliberately: this site exists to be
# cited. Change to Disallow if that stops being true.
User-agent: GPTBot
Allow: /

User-agent: ClaudeBot
Allow: /

User-agent: PerplexityBot
Allow: /

User-agent: Google-Extended
Allow: /

# Build output and raw data have no reason to be indexed as pages.
User-agent: *
Disallow: /build/

Sitemap: {sitemap}
"""


def render_feed(cfg: SiteConfig, deals: list[Deal], updated_iso: str) -> str:
    """RSS of the deals that moved. A window site with no feed gives regular
    readers no way to come back without remembering to."""
    now = datetime.now(timezone.utc).strftime("%a, %d %b %Y %H:%M:%S +0000")
    items = []
    for d in sorted(deals, key=lambda x: -x.cred)[:30]:
        link = cfg.url(deal_path(d))
        items.append(
            "<item>"
            f"<title>{e(f'{d.p} to {d.to}: credibility {d.cred}/100')}</title>"
            f"<link>{e(link)}</link>"
            f"<guid isPermaLink=\"true\">{e(link)}</guid>"
            f"<description>{e(deal_sentence(d))}</description>"
            f"<pubDate>{now}</pubDate>"
            "</item>"
        )
    return (
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<rss version="2.0"><channel>'
        f"<title>{e(cfg.title)}</title>"
        f"<link>{e(cfg.url('/'))}</link>"
        f"<description>{e(cfg.description)}</description>"
        f"<language>en-gb</language><lastBuildDate>{now}</lastBuildDate>"
        f"{''.join(items)}"
        "</channel></rss>\n"
    )


def render_llms_txt(cfg: SiteConfig, deals: list[Deal], raw: dict, updated: str) -> str:
    """An emerging convention: a plain-text map of the site for assistants.
    Cheap to generate, and it states the methodology in the one place a model
    is most likely to read it."""
    window = (raw.get("config") or {}).get("windowName", "")
    lines = [
        f"# {cfg.title}",
        "",
        f"> {cfg.description}",
        "",
        f"Window: {window}. Last updated: {updated}.",
        "",
        "## Method",
        "",
        "Credibility scores are computed in code, not written by a model. A score "
        "rises with the tier of the outlets reporting a claim and with independent "
        "corroboration, and decays when a deal goes quiet. Only tier 1 and tier 2 "
        "outlets can advance a deal's status. A completed transfer requires a tier 1 "
        "source. Scores are an assessment of how well supported a claim is. They are "
        "not predictions and not betting advice.",
        "",
        "## Pages",
        "",
        f"- [Window index]({cfg.url('/')}): every tracked deal, funnel and club summary",
    ]
    for d in sorted(deals, key=lambda x: -x.cred):
        lines.append(
            f"- [{d.p}, {d.from_club} to {d.to}]({cfg.url(deal_path(d))}): "
            f"credibility {d.cred}/100"
        )
    return "\n".join(lines) + "\n"


