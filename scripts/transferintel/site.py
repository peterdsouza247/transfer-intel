"""Phase 7: make the site legible to something that is not a browser.

The problem this solves is not that the site lacks a sitemap. It is that the
site has almost no HTML. Every deal, score, verdict and club dashboard is
written into an empty `<div>` by JavaScript at runtime, so a crawler that does
not execute scripts sees four headings and some empty containers. Google will
usually render JavaScript eventually; Bing is patchier, and the social and
chat crawlers that build link previews do not run it at all. A sitemap
pointing at an empty page just tells more crawlers where the empty page is.

So the order of work here is deliberate:

1. Pre-render the real content into the existing containers. The site's own
   JavaScript overwrites them on load, so the browser experience is
   unchanged and the crawler experience goes from nothing to everything.
2. Give the content URLs. One page with four tabs is one indexable document,
   and "Arsenal transfer spend" has nowhere to land. Club and deal pages turn
   one thin URL into dozens of specific ones.
3. Only then, the plumbing: sitemap, robots, feed, structured data.

Everything is generated from `data.json` at build time, so it cannot drift
from what the pipeline believes.
"""

