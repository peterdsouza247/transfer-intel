def render_club_page(
    cfg: SiteConfig, club: str, club_data: dict, deals: list[Deal],
    updated_iso: str, updated: str,
) -> str:
    incoming = [d for d in deals if d.to == club]
    outgoing = [d for d in deals if d.from_club == club]
    done = [d for d in incoming if str(getattr(d.status, "value", d.status)) == "done"]
    spend = sum(d.fee or 0 for d in done)

    title = f"{club} transfer news, spend and rumour credibility | {cfg.title}"
    description = (
        f"{club}: {len(incoming)} tracked incoming deals, £{spend:g}m committed. "
        "Every rumour scored 0 to 100 for credibility with sources shown."
    )

    def table(rows: list[Deal], other: str) -> str:
        if not rows:
            return "<p class=\"lede\">Nothing tracked yet this window.</p>"
        cells = "".join(
            f"<tr><td><a href=\"{e(cfg.href(deal_path(d)))}\">{e(d.p)}</a></td>"
            f"<td>{e(d.from_club if other == 'from' else d.to)}</td>"
            f"<td>{f'£{d.fee:g}m' if d.fee else 'Free'}</td>"
            f"<td>{e(STATUS_LABEL.get(str(getattr(d.status,'value',d.status)), ''))}</td>"
            f"<td>{d.cred}</td></tr>"
            for d in sorted(rows, key=lambda x: -x.cred)
        )
        head = "From" if other == "from" else "To"
        return (
            f"<table><thead><tr><th>Player</th><th>{head}</th><th>Fee</th>"
            f"<th>Status</th><th>Cred</th></tr></thead><tbody>{cells}</tbody></table>"
        )

    needs = club_data.get("needs") or []
    needs_html = (
        "<ul>" + "".join(f"<li>{e(n)}</li>" for n in needs) + "</ul>" if needs else ""
    )

    body = f"""
<h1>{e(club)} transfer window</h1>
<p class="lede">{e(description)}</p>
<div class="panel">
<table>
<tr><th>Tracked incoming</th><td>{len(incoming)}</td></tr>
<tr><th>Completed</th><td>{len(done)}</td></tr>
<tr><th>Committed spend</th><td>£{spend:g}m</td></tr>
<tr><th>Tracked outgoing</th><td>{len(outgoing)}</td></tr>
</table>
</div>
{f"<h2>Squad needs</h2>{needs_html}" if needs_html else ""}
<h2>Incoming</h2>
{table(incoming, "from")}
<h2>Outgoing</h2>
{table(outgoing, "to")}
<p><a href="{e(cfg.href("/"))}">Back to the full window index</a></p>
"""
    ld = script_ld(
        jsonld_breadcrumb(cfg, [("Home", "/"), (club, club_path(club))]),
        {
            "@context": "https://schema.org",
            "@type": "SportsTeam",
            "name": club,
            "sport": "Association football",
            "url": cfg.url(club_path(club)),
        },
    )
    crumb = f'<a href="{e(cfg.href("/"))}">TransferIntel</a> / {e(club)}'
    return standalone_page(
        cfg, title=title, description=description, path=club_path(club),
        og_image=f"og/club-{slugify(club)}.png", updated_iso=updated_iso,
        breadcrumb=crumb, body=body, ld=ld,
    )


