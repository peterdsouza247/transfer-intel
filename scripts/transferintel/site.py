# ---------------------------------------------------------------- config


@dataclass
class SiteConfig:
    """Everything the renderer needs that is not in the deal data.

    `base_url` is the one value that has to be right. Canonical tags, Open
    Graph URLs and the sitemap all require absolute URLs, and a wrong origin
    is worse than none: it tells crawlers the real page is somewhere else.
    """

    base_url: str = ""
    title: str = "TransferIntel"
    description: str = (
        "Premier League transfer rumours scored 0 to 100 for credibility, "
        "with transparent value-for-money verdicts on confirmed fees."
    )
    author: str = "Peter Brendan"
    twitter: str = ""
    locale: str = "en_GB"

    @classmethod
    def from_data(cls, raw: dict) -> "SiteConfig":
        site = (raw.get("config") or {}).get("site") or {}
        return cls(
            base_url=str(site.get("baseUrl", "")).rstrip("/"),
            title=site.get("title", cls.title),
            description=site.get("description", cls.description),
            author=site.get("author", cls.author),
            twitter=site.get("twitter", ""),
            locale=site.get("locale", cls.locale),
        )

    @property
    def path_prefix(self) -> str:
        """The sub-path a project site is served from, e.g. /transferintel."""
        if not self.base_url:
            return ""
        after_scheme = self.base_url.split("://", 1)[-1]
        slash = after_scheme.find("/")
        return "" if slash == -1 else after_scheme[slash:].rstrip("/")

    def url(self, path: str) -> str:
        return f"{self.base_url}/{path.lstrip('/')}" if self.base_url else f"/{path.lstrip('/')}"

    def href(self, path: str) -> str:
        """Root-relative link, correct whether or not the site sits in a sub-path."""
        return f"{self.path_prefix}/{path.lstrip('/')}" if path != "/" else f"{self.path_prefix}/"


def slugify(value: str) -> str:
    """URL-safe slug.

    Uses the same transliteration table as entity resolution rather than a
    second copy. NFKD does not decompose characters like O-slash, so a naive
    normalise silently drops them: Odegaard would slug as "degaard", losing
    the first letter of the name and risking a collision with a different
    player. One table, used everywhere, or this bug comes back.
    """
    norm = fold_for_slug(value)
    norm = re.sub(r"[^a-z0-9]+", "-", norm).strip("-")
    return norm or "item"


def e(value) -> str:
    return html.escape(str(value if value is not None else ""), quote=True)


# ------------------------------------------------------- HTML injection


class _Locator(HTMLParser):
    """Find an element by id and report where its content begins and ends.

    A regex over HTML is how you corrupt a file the first time someone adds an
    attribute or a nested tag. This walks the document properly and counts
    depth, so injecting into `<div id="feed">` still works after the div grows
    children or the attribute order changes.
    """

    def __init__(self, target_id: str) -> None:
        super().__init__(convert_charrefs=False)
        self.target = target_id
        self.tag: str | None = None
        self.depth = 0
        self.start: tuple[int, int] | None = None
        self.end: tuple[int, int] | None = None

    def handle_starttag(self, tag, attrs):
        attrs_d = dict(attrs)
        if self.tag is None and attrs_d.get("id") == self.target:
            self.tag = tag
            self.depth = 1
            line, col = self.getpos()
            self.start = (line, col + len(self.get_starttag_text() or ""))
        elif self.tag is not None and tag == self.tag:
            self.depth += 1

    def handle_startendtag(self, tag, attrs):
        if dict(attrs).get("id") == self.target and self.tag is None:
            self.tag = tag
            self.start = None  # self-closing, nothing to fill

    def handle_endtag(self, tag):
        if self.tag is not None and tag == self.tag and self.end is None:
            self.depth -= 1
            if self.depth == 0:
                self.end = self.getpos()


def _offset(text: str, line: int, col: int) -> int:
    lines = text.splitlines(keepends=True)
    return sum(len(l) for l in lines[: line - 1]) + col


def inject_into(document: str, element_id: str, content: str) -> str:
    """Replace the inner HTML of `element_id`. Returns the document unchanged
    if the element is not found, because a missing container is a template the
    author has edited, not a reason to fail a daily build."""
    loc = _Locator(element_id)
    try:
        loc.feed(document)
        loc.close()
    except Exception:
        return document
    if not loc.start or not loc.end:
        return document
    start = _offset(document, *loc.start)
    end = _offset(document, *loc.end)
    if start > end:
        return document
    return document[:start] + content + document[end:]


def inject_head(document: str, content: str) -> str:
    idx = document.lower().rfind("</head>")
    if idx == -1:
        return document
    return document[:idx] + content + document[idx:]


# ------------------------------------------------------------- fragments


def deal_slug(deal: Deal) -> str:
    return slugify(deal.id)


def deal_path(deal: Deal) -> str:
    return f"deals/{deal_slug(deal)}/"


def club_path(club: str) -> str:
    return f"clubs/{slugify(club)}/"


def cred_class(cred: int) -> str:
    if cred >= 75:
        return "high"
    if cred >= 50:
        return "mid"
    if cred >= 25:
        return "low"
    return "vlow"


def deal_sentence(deal: Deal) -> str:
    """One plain sentence per deal, the thing a crawler and a screen reader
    both actually read."""
    fee = f" for a reported £{deal.fee:g}m" if deal.fee else ""
    status = STATUS_LABEL.get(
        deal.status.value if hasattr(deal.status, "value") else str(deal.status),
        str(deal.status),
    )
    return (
        f"{deal.p}, {deal.pos or 'player'}"
        f"{f', age {deal.age}' if deal.age else ''}, "
        f"from {deal.from_club} to {deal.to}{fee}. "
        f"Status: {status}. Credibility {deal.cred} out of 100, "
        f"based on {deal.src or 'reported sources'}."
    )


def render_deal_list(deals: Iterable[Deal], cfg: SiteConfig) -> str:
    """The pre-rendered rumour list. The site's JS replaces this on load."""
    rows = []
    for d in deals:
        status = d.status.value if hasattr(d.status, "value") else str(d.status)
        rows.append(
            f'<article class="rumor" id="deal-{e(d.id)}">'
            f'<div class="cred {cred_class(d.cred)}" aria-label="Credibility {d.cred} of 100">'
            f"<span>{d.cred}</span></div>"
            f"<div><h3><a href=\"{e(cfg.href(deal_path(d)))}\">{e(d.p)}</a></h3>"
            f"<p>{e(deal_sentence(d))}</p>"
            f'<p class="meta">{e(STATUS_LABEL.get(status, status))}'
            f"{f' · £{d.fee:g}m' if d.fee else ''}"
            f"{f' · {e(d.src)}' if d.src else ''}"
            f"{f' · updated {e(d.date)}' if d.date else ''}</p>"
            f"{f'<p class=\"note\">{e(d.note)}</p>' if d.note else ''}"
            "</div></article>"
        )
    return "\n".join(rows)


def render_club_grid(clubs: dict, deals: list[Deal], cfg: SiteConfig) -> str:
    cards = []
    for club in sorted(clubs):
        incoming = [d for d in deals if d.to == club]
        spend = sum(d.fee or 0 for d in incoming if str(getattr(d.status, "value", d.status)) == "done")
        cards.append(
            f'<article class="clubcard"><h3>'
            f'<a href="{e(cfg.href(club_path(club)))}">{e(club)}</a></h3>'
            f"<p>{len(incoming)} tracked deals, £{spend:g}m committed this window.</p>"
            "</article>"
        )
    return "\n".join(cards)


def render_funnel(deals: list[Deal]) -> str:
    counts = {s: 0 for s in STATUS_ORDER}
    for d in deals:
        key = str(getattr(d.status, "value", d.status))
        counts[key] = counts.get(key, 0) + 1
    chips = [
        f'<div class="stagechip"><div class="n">{counts[s]}</div>'
        f"<div>{e(STATUS_LABEL[s])}</div></div>"
        for s in STATUS_ORDER
    ]
    return "\n".join(chips)


def render_feed_items(deals: list[Deal]) -> str:
    recent = sorted(deals, key=lambda d: (d.cred, d.p), reverse=True)[:12]
    items = []
    for d in recent:
        items.append(
            f'<div class="item"><div class="when">{e(d.date or "")}</div>'
            f'<div class="what"><b>{e(d.p)}</b> '
            f'<span class="meta">{e(deal_sentence(d))}</span></div></div>'
        )
    return "\n".join(items)


# ---------------------------------------------------------- structured data


def jsonld_website(cfg: SiteConfig, raw: dict) -> dict:
    return {
        "@context": "https://schema.org",
        "@type": "WebSite",
        "name": cfg.title,
        "url": cfg.url("/"),
        "description": cfg.description,
        "inLanguage": "en-GB",
        "author": {"@type": "Person", "name": cfg.author},
    }


def jsonld_dataset(cfg: SiteConfig, raw: dict, deals: list[Deal], updated: str) -> dict:
    """The site is, structurally, a dataset with a published methodology.
    Saying so is the most honest description available and it is the schema
    type most likely to be understood by something other than a search
    engine."""
    window = (raw.get("config") or {}).get("windowName", "")
    return {
        "@context": "https://schema.org",
        "@type": "Dataset",
        "name": f"{cfg.title}: {window} transfer credibility index".strip(),
        "description": (
            f"{len(deals)} tracked Premier League transfers, each scored 0 to 100 "
            "for credibility from source tier and corroboration, with fee and "
            "status history."
        ),
        "url": cfg.url("/"),
        "creator": {"@type": "Person", "name": cfg.author},
        "dateModified": updated,
        "isAccessibleForFree": True,
        "license": "https://creativecommons.org/licenses/by/4.0/",
        "variableMeasured": [
            {"@type": "PropertyValue", "name": "credibility", "minValue": 0, "maxValue": 100},
            {"@type": "PropertyValue", "name": "status"},
            {"@type": "PropertyValue", "name": "fee", "unitText": "GBP millions"},
        ],
    }


def jsonld_itemlist(cfg: SiteConfig, deals: list[Deal]) -> dict:
    return {
        "@context": "https://schema.org",
        "@type": "ItemList",
        "itemListElement": [
            {
                "@type": "ListItem",
                "position": i + 1,
                "url": cfg.url(deal_path(d)),
                "name": f"{d.p}: {d.from_club} to {d.to}",
            }
            for i, d in enumerate(deals)
        ],
    }


def jsonld_claimreview(cfg: SiteConfig, deal: Deal, updated: str) -> dict:
    """A credibility score on a reported transfer is, precisely, a review of a
    claim. The markup is valid regardless; Google's fact-check rich results
    additionally require acceptance into their programme, so treat any rich
    result as upside rather than the reason for doing this."""
    return {
        "@context": "https://schema.org",
        "@type": "ClaimReview",
        "url": cfg.url(deal_path(deal)),
        "datePublished": updated,
        "claimReviewed": f"{deal.p} is joining {deal.to} from {deal.from_club}.",
        "author": {"@type": "Organization", "name": cfg.title, "url": cfg.url("/")},
        "itemReviewed": {
            "@type": "Claim",
            "appearance": [{"@type": "CreativeWork", "url": ev.url} for ev in deal.evidence[:5]],
            "firstAppearance": {
                "@type": "CreativeWork",
                "url": deal.evidence[0].url if deal.evidence else cfg.url("/"),
            },
        },
        "reviewRating": {
            "@type": "Rating",
            "ratingValue": deal.cred,
            "bestRating": 100,
            "worstRating": 0,
            "alternateName": STATUS_LABEL.get(
                str(getattr(deal.status, "value", deal.status)), ""
            ),
        },
    }


def jsonld_breadcrumb(cfg: SiteConfig, trail: list[tuple[str, str]]) -> dict:
    return {
        "@context": "https://schema.org",
        "@type": "BreadcrumbList",
        "itemListElement": [
            {"@type": "ListItem", "position": i + 1, "name": name, "item": cfg.url(path)}
            for i, (name, path) in enumerate(trail)
        ],
    }


def script_ld(*blocks: dict) -> str:
    return "\n".join(
        '<script type="application/ld+json">'
        + json.dumps(b, ensure_ascii=False, separators=(",", ":"))
        + "</script>"
        for b in blocks
    )


# ---------------------------------------------------------------- head tags


def head_tags(
    cfg: SiteConfig,
    *,
    title: str,
    description: str,
    path: str,
    og_image: str,
    updated_iso: str,
    extra: str = "",
) -> str:
    canonical = cfg.url(path)
    tags = [
        GEN_START,
        f'<meta name="description" content="{e(description)}">',
        f'<link rel="canonical" href="{e(canonical)}">',
        '<meta name="robots" content="index,follow,max-image-preview:large,max-snippet:-1">',
        f'<meta property="og:type" content="website">',
        f'<meta property="og:site_name" content="{e(cfg.title)}">',
        f'<meta property="og:title" content="{e(title)}">',
        f'<meta property="og:description" content="{e(description)}">',
        f'<meta property="og:url" content="{e(canonical)}">',
        f'<meta property="og:locale" content="{e(cfg.locale)}">',
        f'<meta property="og:image" content="{e(cfg.url(og_image))}">',
        '<meta property="og:image:width" content="1200">',
        '<meta property="og:image:height" content="630">',
        f'<meta property="og:image:alt" content="{e(title)}">',
        '<meta name="twitter:card" content="summary_large_image">',
        f'<meta name="twitter:title" content="{e(title)}">',
        f'<meta name="twitter:description" content="{e(description)}">',
        f'<meta name="twitter:image" content="{e(cfg.url(og_image))}">',
    ]
    if cfg.twitter:
        tags.append(f'<meta name="twitter:site" content="{e(cfg.twitter)}">')
    tags += [
        f'<meta name="author" content="{e(cfg.author)}">',
        f'<meta property="article:modified_time" content="{e(updated_iso)}">',
        f'<link rel="alternate" type="application/rss+xml" title="{e(cfg.title)} updates" '
        f'href="{e(cfg.href("feed.xml"))}">',
        f'<link rel="icon" href="{e(cfg.href("favicon.svg"))}" type="image/svg+xml">',
        '<meta name="theme-color" content="#0b0e14">',
    ]
    if extra:
        tags.append(extra)
    tags.append(GEN_END)
    return "\n".join(tags) + "\n"


