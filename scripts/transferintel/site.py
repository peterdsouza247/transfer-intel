def render_analytics(cfg: SiteConfig) -> str:
    """TI-012. Pageviews plus six named events, under 5KB, no cookies.

    Cloudflare Web Analytics is cookieless and needs no consent banner, which
    is why it is the base layer rather than the more familiar alternative. The
    event helper is deliberately provider-agnostic: it pushes to whichever of
    the two scripts is present and does nothing at all if neither is, so
    removing a provider never leaves a page throwing on every click.
    """
    parts = []
    if cfg.analytics_token:
        parts.append(
            '<script defer src="https://static.cloudflareinsights.com/beacon.min.js" '
            f"data-cf-beacon='{{\"token\": \"{e(cfg.analytics_token)}\"}}'></script>"
        )
    if cfg.goatcounter:
        parts.append(
            f'<script data-goatcounter="{e(cfg.goatcounter)}" '
            'async src="//gc.zgo.at/count.js"></script>'
        )
    parts.append("""<script>
/* TI-012. Six events, one helper, no library. */
(function(){
  function send(name, detail){
    try{
      if(window.goatcounter && window.goatcounter.count){
        window.goatcounter.count({path:name+(detail?":"+detail:""),
                                  title:name, event:true});
      }
      if(window.__cfBeacon){ /* Cloudflare records pageviews only */ }
      window.dispatchEvent(new CustomEvent("ti:event",
        {detail:{name:name, detail:detail}}));
    }catch(err){ /* analytics must never break the page */ }
  }
  window.tiTrack = send;
  document.addEventListener("click", function(ev){
    var form = ev.target.closest && ev.target.closest("form.capture");
    if(form){ send("newsletter_submit", form.dataset.placement); return; }
    var link = ev.target.closest && ev.target.closest("a");
    if(!link) return;
    var href = link.getAttribute("href") || "";
    if(/^https?:\\/\\//.test(href) && link.hostname !== location.hostname){
      send("outbound_source", link.hostname);
    }else if(href.indexOf("/deals/") !== -1){
      send("deal_opened", href.split("/deals/")[1].replace(/\\/$/,""));
    }else if(href.indexOf("/clubs/") !== -1){
      send("club_opened", href.split("/clubs/")[1].replace(/\\/$/,""));
    }
  }, true);
  if(location.pathname.indexOf("/thanks") !== -1){ send("newsletter_confirmed"); }
})();
</script>""")
    return "\n".join(parts)


def best_source_link(deal: Deal) -> Evidence | None:
    """The best piece of evidence a reader can actually open.

    Tier 1 first, then most recent. Migrated records carry `urn:` evidence
    rather than a link, so a large part of the dataset has none and gets no
    link at all. That is the honest outcome: there is nothing to send anyone
    to, and a dead link is worse than no link.
    """
    live = [
        e for e in deal.evidence
        if str(e.url).startswith("http")
    ]
    if not live:
        return None
    live.sort(key=lambda e: (e.tier, -e.date.toordinal()))
    return live[0]


def source_anchor(deal: Deal) -> str:
    best = best_source_link(deal)
    if best is None:
        return ""
    return (
        f' <a class="srclink" href="{e(str(best.url))}" target="_blank" '
        f'rel="noopener nofollow">{e(best.source)} &#8599;</a>'
    )


def pos_badge(deal: Deal) -> str:
    return f'<span class="pos">{e(deal.pos)}</span>' if deal.pos else ""


def render_deal_list(deals: Iterable[Deal], cfg: SiteConfig) -> str:
    """The pre-rendered rumour list. The site's JS replaces this on load."""
    rows = []
    for d in deals:
        status = d.status.value if hasattr(d.status, "value") else str(d.status)
        rows.append(
            f'<article class="rumor" id="deal-{e(d.id)}">'
            f'<div class="cred {cred_class(d.cred)}" aria-label="Credibility {d.cred} of 100">'
            f"<span>{d.cred}</span></div>"
            f"<div><h3><a href=\"{e(cfg.href(deal_path(d)))}\">{e(d.p)}</a>"
            f"{pos_badge(d)}</h3>"
            f"<p>{e(deal_sentence(d))}</p>"
            f'<p class="meta">{e(STATUS_LABEL.get(status, status))}'
            f"{f' · £{d.fee:g}m' if d.fee else ''}"
            f"{f' · {e(d.src)}' if d.src else ''}"
            f"{f' · updated {e(d.date)}' if d.date else ''}"
            f"{source_anchor(d)}</p>"
            f"{f'<p class=\"note\">{e(d.note)}</p>' if d.note else ''}"
            "</div></article>"
        )
    return "\n".join(rows)


def render_club_grid(clubs: dict, deals: list[Deal], cfg: SiteConfig) -> str:
    cards = []
    for club in sorted(clubs):
        incoming = [d for d in deals if d.to == club]
        spend = sum(d.fee or 0 for d in incoming if str(getattr(d.status, "value", d.status)) == "done")
        cards.append(
            f'<article class="clubcard"><h3>'
            f'<a href="{e(cfg.href(club_path(club)))}">{e(club)}</a></h3>'
            f"<p>{len(incoming)} tracked deals, £{spend:g}m committed this window.</p>"
            "</article>"
        )
    return "\n".join(cards)


def render_funnel(deals: list[Deal]) -> str:
    counts = {s: 0 for s in STATUS_ORDER}
    for d in deals:
        key = str(getattr(d.status, "value", d.status))
        counts[key] = counts.get(key, 0) + 1
    chips = [
        f'<div class="stagechip"><div class="n">{counts[s]}</div>'
        f"<div>{e(STATUS_LABEL[s])}</div></div>"
        for s in STATUS_ORDER
    ]
    return "\n".join(chips)


#: The pulse feed's default ordering, mirrored exactly in index.html.
#:
#: Collapsed first because a dead deal is the most surprising thing on the
#: page, then the stages closest to resolution, then the noise. Completed
#: deals sit near the bottom deliberately: they are settled, and a pulse is
#: about what is still moving.
FEED_RANK: dict[str, int] = {
    "collapsed": 0, "medical": 1, "confirmed": 2, "agreed": 3,
    "talks": 4, "done": 5, "rumor": 6,
}

_MONTHS = {m: i for i, m in enumerate(
    ["jan", "feb", "mar", "apr", "may", "jun",
     "jul", "aug", "sep", "oct", "nov", "dec"], start=1)}


def display_date_key(label: str) -> tuple[int, int]:
    """Sort key for the site's display dates, e.g. "Jul 8".

    Returns (0, 0) for anything unparseable, which sorts such rows last under
    a descending sort. A deal with no date is not recent news.
    """
    parts = (label or "").replace(",", " ").split()
    if len(parts) < 2:
        return (0, 0)
    month = _MONTHS.get(parts[0][:3].lower())
    if month is None:
        return (0, 0)
    try:
        return (month, int(parts[1]))
    except ValueError:
        return (month, 0)


def feed_order(deals: Iterable[Deal], mode: str = "active") -> list[Deal]:
    """Order the pulse feed. Must match the `FEED_SORTS` table in index.html.

    The pre-rendered feed used to sort by credibility then player name, while
    the browser sorted by stage then fee. The list therefore reordered itself
    the moment JavaScript ran, and readers without it got twelve completed
    deals in reverse alphabetical order, which is not a pulse.
    """
    rows = list(deals)
    if mode == "recent":
        return sorted(rows, key=lambda d: (display_date_key(d.date), d.fee or 0),
                      reverse=True)
    if mode == "fee":
        return sorted(rows, key=lambda d: (-(d.fee or 0), d.p))
    if mode == "cred":
        return sorted(rows, key=lambda d: (-d.cred, d.p))
    return sorted(
        rows,
        key=lambda d: (FEED_RANK.get(
            d.status.value if hasattr(d.status, "value") else str(d.status), 9),
            -(d.fee or 0), d.p),
    )


def render_feed_items(deals: list[Deal], mode: str = "active",
                      limit: int = 12) -> str:
    items = []
    for d in feed_order(deals, mode)[:limit]:
        items.append(
            f'<div class="item"><div class="when">{e(d.date or "")}</div>'
            f'<div class="what"><b>{e(d.p)}</b>{pos_badge(d)} '
            f'<span class="meta">{e(deal_sentence(d))}'
            f'{source_anchor(d)}</span></div></div>'
        )
    return "\n".join(items)


